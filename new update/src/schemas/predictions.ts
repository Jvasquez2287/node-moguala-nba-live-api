import * as Joi from 'joi';

/**
 * Insight about why the prediction was made
 */
export interface GamePredictionInsight {
  title: string;
  description: string;
  impact: string;
}

/**
 * Key factor driving the prediction
 */
export interface KeyDriver {
  factor: string;
  impact: string;
  magnitude: 'High' | 'Moderate' | 'Low';
}

/**
 * Risk or uncertainty factor that could affect the prediction
 */
export interface RiskFactor {
  factor: string;
  explanation: string;
}

/**
 * Prediction for a single game
 */
export interface GamePrediction {
  game_id: string;
  home_team_id: number;
  home_team_name: string;
  away_team_id: number;
  away_team_name: string;
  game_date: string;

  // Predictions
  home_win_probability: number;
  away_win_probability: number;
  predicted_home_score: number;
  predicted_away_score: number;
  confidence: number;

  // Insights
  insights: GamePredictionInsight[];

  // Team stats used for prediction
  home_team_win_pct: number;
  away_team_win_pct: number;
  home_team_net_rating?: number;
  away_team_net_rating?: number;

  // Enhanced AI analysis (optional, generated by Groq)
  confidence_tier?: 'high' | 'medium' | 'low';
  confidence_explanation?: string;
  key_drivers?: KeyDriver[];
  risk_factors?: RiskFactor[];
  matchup_narrative?: string;
}

/**
 * Response for game predictions
 */
export interface PredictionsResponse {
  date: string;
  predictions: GamePrediction[];
  season: string;
}

// Joi validation schemas
export const gamePredictionInsightSchema = Joi.object({
  title: Joi.string().required(),
  description: Joi.string().required(),
  impact: Joi.string().required(),
});

export const keyDriverSchema = Joi.object({
  factor: Joi.string().required(),
  impact: Joi.string().required(),
  magnitude: Joi.string().valid('High', 'Moderate', 'Low').required(),
});

export const riskFactorSchema = Joi.object({
  factor: Joi.string().required(),
  explanation: Joi.string().required(),
});

export const gamePredictionSchema = Joi.object({
  game_id: Joi.string().required(),
  home_team_id: Joi.number().integer().required(),
  home_team_name: Joi.string().required(),
  away_team_id: Joi.number().integer().required(),
  away_team_name: Joi.string().required(),
  game_date: Joi.string().pattern(/^\d{4}-\d{2}-\d{2}$/).required(),

  // Predictions
  home_win_probability: Joi.number().min(0).max(1).required(),
  away_win_probability: Joi.number().min(0).max(1).required(),
  predicted_home_score: Joi.number().min(0).required(),
  predicted_away_score: Joi.number().min(0).required(),
  confidence: Joi.number().min(0).max(1).required(),

  // Insights
  insights: Joi.array().items(gamePredictionInsightSchema).default([]),

  // Team stats used for prediction
  home_team_win_pct: Joi.number().min(0).max(1).required(),
  away_team_win_pct: Joi.number().min(0).max(1).required(),
  home_team_net_rating: Joi.number().optional(),
  away_team_net_rating: Joi.number().optional(),

  // Enhanced AI analysis
  confidence_tier: Joi.string().valid('high', 'medium', 'low').optional(),
  confidence_explanation: Joi.string().optional(),
  key_drivers: Joi.array().items(keyDriverSchema).optional(),
  risk_factors: Joi.array().items(riskFactorSchema).optional(),
  matchup_narrative: Joi.string().optional(),
});

export const predictionsResponseSchema = Joi.object({
  date: Joi.string().pattern(/^\d{4}-\d{2}-\d{2}$/).required(),
  predictions: Joi.array().items(gamePredictionSchema).required(),
  season: Joi.string().required(),
});